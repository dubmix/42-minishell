#include "../../minishell.h"

int    pwd(void)
{
    char    *cwd;
    cwd = NULL;
    if (getcwd(cwd, _PC_PATH_MAX))
    {
        printf("%s", cwd);
        return (1);
    }
    else
        return (0);
}